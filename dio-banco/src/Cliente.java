public class Cliente {
    private String nome;
    private String SobreNome;
    private String cpf;
    private int idade;
    private String cep;

    public String getNome() {
        return nome;
    }

    public String getSobreNome() {
        return SobreNome;
    }

    public String getCpf() {
        return cpf;
    }

    public int getIdade() {
        return idade;
    }

    public String getCep() {
        return cep;
    }
}
